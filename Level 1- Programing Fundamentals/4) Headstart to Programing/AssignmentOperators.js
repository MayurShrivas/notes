// Assignment Operator also known as Shorthand Operators.
let num = 20;
num = num + 10;
console.log (num); // Prints 30

num += 10; // JS will convert shorthand opeartors num += 10 to num + 10
console.log (num); // print 40

let num2 = 20;
num2 -= 10;
console.log (num2); // prints 10

// use /= for divide
// use *= for multiply
// use += for add
// use -= for substract