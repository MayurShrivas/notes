// Easy way to print "Hello World" as many times u want.

for (let i = 1; i <= 20; i++)
{
    console.log ("Hello World")
}
// 1) let i = 1 ---> is Initialization section;
// 2) i <= 20 ---> is a condition section;
// 3) { ------ }  ---> matter between this is a loop body;
// 4) i++ ---> update section;
