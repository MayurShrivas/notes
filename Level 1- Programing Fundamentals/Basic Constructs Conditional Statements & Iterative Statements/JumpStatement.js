 // Break Statements :
 // Breaks brings the control outside of the loop.
 for (let i = 0; i <= 5; i++)
 {
    if(i === 3)
    {
        break;
    }
    console.log (i);
 }

 console.log ("Loop Exited");


 // Continue Statement :
 // Countinue moves you to the next iteration, Skipping the rest of the code 
 for (let i = 0; i <= 5; i++)
 {
    if(i === 3)
    {
        continue;
    }
    console.log (i);
 }

 console.log ("Loop Exited");